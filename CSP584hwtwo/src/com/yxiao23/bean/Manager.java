package com.yxiao23.bean;

public class Manager {
	private String ManagerAc;//manager account
	private String ManagerName;
	private String ManagerPswd;
	private String ManagerEmail;
	public String getManagerAc() {
		return ManagerAc;
	}
	public void setManagerAc(String managerAc) {
		ManagerAc = managerAc;
	}
	public String getManagerName() {
		return ManagerName;
	}
	public void setManagerName(String managerName) {
		ManagerName = managerName;
	}
	public String getManagerPswd() {
		return ManagerPswd;
	}
	public void setManagerPswd(String managerPswd) {
		ManagerPswd = managerPswd;
	}
	public String getManagerEmail() {
		return ManagerEmail;
	}
	public void setManagerEmail(String managerEmail) {
		ManagerEmail = managerEmail;
	}
	
	
}
